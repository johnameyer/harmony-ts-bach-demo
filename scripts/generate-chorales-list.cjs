#!/usr/bin/env node
// Generates src/app/data/chorales-list.generated.ts from the
// node_modules/Bach_chorale_FB dev dependency.
// Run automatically via the prebuild/pretest/prestart npm scripts.

const fs = require('fs');
const path = require('path');

const choralesDir = path.join(
  __dirname,
  '../node_modules/Bach_chorale_FB/FB_source/musicXML_master',
);

const outputFile = path.join(__dirname, '../src/app/data/chorales-list.generated.ts');

const files = fs
  .readdirSync(choralesDir)
  .filter((f) => f.endsWith('.musicxml'))
  .sort();

const content = `// Auto-generated by scripts/generate-chorales-list.cjs — do not edit manually.
// Re-generated on every build/test/start via the pre* npm scripts.
export const CHORALE_FILES: readonly string[] = ${JSON.stringify(files, null, 2)};
`;

fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(outputFile, content, 'utf8');
console.log(`Generated chorales list: ${files.length} files → ${outputFile}`);
